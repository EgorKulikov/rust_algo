type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let n = input.read_size();
    let m = input.read_size();
    let q = input.read_size();
    let edges = input.read_size_pair_vec(m).dec();

    type Mod = ModInt7;
    let mut matrix = Matrix::zero(n, n);
    for (u, v) in edges {
        matrix[(u, v)] += Mod::one();
        matrix[(v, u)] += Mod::one();
    }
    let mut pw = Vec::with_capacity(25);
    for _ in 0..25 {
        pw.push(matrix.clone());
        matrix = matrix.mult(&matrix);
    }
    for _ in 0..q {
        let v = input.read_size() - 1;
        let k = input.read_size();

        let mut res = Matrix::zero(1, n);
        res[(0, v)] = Mod::one();
        for i in 0..25 {
            if k.is_set(i) {
                res = res.mult(&pw[i]);
            }
        }
        out.print_line(res.iter().fold(Mod::zero(), |acc, x| acc + *x));
    }
}

pub static TEST_TYPE: LegacyTestType = LegacyTestType::Single;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    let mut pre_calc = ();

    match TEST_TYPE {
        LegacyTestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        LegacyTestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        LegacyTestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
    }
    output.flush();
    match TASK_TYPE {
        TaskType::Classic => input.is_empty(),
        TaskType::Interactive => true,
    }
}

