fn solve(input: &mut Input, _test_case: usize) {
    type Mod = ModIntF;

    let n = input.read_usize();
    let q = input.read_usize();
    let a = input.read_vec::<Mod>(n);

    #[derive(Clone, Copy)]
    struct Node {
        sum: Mod,
        a: Mod,
        b: Mod,
    }

    impl Default for Node {
        fn default() -> Self {
            Node {
                sum: Mod::zero(),
                a: Mod::one(),
                b: Mod::zero(),
            }
        }
    }

    impl SegmentTreeNode for Node {
        fn new(_left: usize, _right: usize) -> Self {
            Node::default()
        }

        fn join(
            &mut self,
            left_val: &Self,
            right_val: &Self,
            _left: usize,
            _mid: usize,
            _right: usize,
        ) {
            self.sum = left_val.sum + right_val.sum;
        }

        fn accumulate(&mut self, value: &Self, left: usize, right: usize) {
            self.sum = value.a * self.sum + value.b * Mod::from_index(right - left);
            self.a = value.a * self.a;
            self.b = value.a * self.b + value.b;
        }

        fn reset_delta(&mut self, _left: usize, _right: usize) {
            self.a = Mod::one();
            self.b = Mod::zero();
        }
    }

    impl QueryResult<Mod> for Node {
        fn empty_result() -> Mod {
            Mod::zero()
        }

        fn result(&self) -> Mod {
            self.sum
        }

        fn join_results(
            left_res: Mod,
            right_res: Mod,
            _left: usize,
            _mid: usize,
            _right: usize,
        ) -> Mod {
            left_res + right_res
        }
    }

    let mut tree = SegmentTree::from_generator(n, |i| Node {
        sum: a[i],
        a: Mod::one(),
        b: Mod::zero(),
    });

    for _ in 0..q {
        let t = input.read_usize();
        if t == 0 {
            let l = input.read_usize();
            let r = input.read_usize();
            let b = input.read::<Mod>();
            let c = input.read::<Mod>();
            tree.update(
                l..r,
                &Node {
                    sum: Mod::zero(),
                    a: b,
                    b: c,
                },
            );
        } else {
            let l = input.read_usize();
            let r = input.read_usize();
            let ans: Mod = tree.query(l..r);
            out_line!(ans);
        }
    }
}

pub(crate) fn run(mut input: Input) -> bool {
    #[allow(dead_code)]
    enum TestType {
        Single,
        MultiNumber,
        MultiEof,
    }
    let test_type = TestType::Single;
    match test_type {
        TestType::Single => solve(&mut input, 1),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 0usize..t {
                solve(&mut input, i + 1);
            }
        }
        TestType::MultiEof => {
            let mut i = 1usize;
            while input.peek().is_some() {
                solve(&mut input, i);
                i += 1;
            }
        }
    }
    output().flush();
    input.skip_whitespace();
    !input.peek().is_some()
}

