type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let n = input.read_size();
    let m = input.read_size();
    let q = input.read_size();
    let s = input.read_size();
    let edges = input.read_vec::<(usize, usize, i64)>(m);
    if n == 0 {
        return;
    }

    let graph = Graph::new(n).do_with(|graph| {
        for (u, v, w) in edges {
            graph.add_edge(WeightedEdge::new(u, v, w));
        }
    });
    let distances = graph.negative_distances_from(s);
    for _ in 0..q {
        let d = input.read_size();
        let distance = distances[d];
        match distance {
            Distance::None => out.print_line("Impossible"),
            Distance::Finite { distance, .. } => out.print_line(distance),
            Distance::Infinite => out.print_line("-Infinity"),
        }
    }
}

pub static TEST_TYPE: TestType = TestType::MultiEof;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    let mut pre_calc = ();

    match TEST_TYPE {
        TestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        TestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
    }
    output.flush();
    match TASK_TYPE {
        TaskType::Classic => input.is_empty(),
        TaskType::Interactive => true,
    }
}

