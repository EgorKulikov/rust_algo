type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let n = input.read_size();
    let _m = input.read_size();
    let s = input.read_size_vec(n);

    let sa = SuffixArray::new(&s);
    let mut dsu = DSU::new(s.len() + 1);
    let order = s
        .indices()
        .collect::<Vec<_>>()
        .sorted_by_key(|&i| Reverse(sa.lcp(i, i + 1)));
    let mut ans = n;
    let mut from = 0;
    let mut len = n;
    for i in order {
        let cand = sa.lcp(i, i + 1) * (dsu.size(i) + dsu.size(i + 1));
        if ans.maxim(cand) {
            from = sa[i];
            len = sa.lcp(i, i + 1);
        }
        dsu.union(i, i + 1);
    }
    out.print_line(ans);
    out.print_line(len);
    out.print_line(&s[from..from + len]);
}

pub static TEST_TYPE: LegacyTestType = LegacyTestType::Single;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    let mut pre_calc = ();

    match TEST_TYPE {
        LegacyTestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        LegacyTestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        LegacyTestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
    }
    output.flush();
    match TASK_TYPE {
        TaskType::Classic => input.is_empty(),
        TaskType::Interactive => true,
    }
}

