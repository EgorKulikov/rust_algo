type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let v0 = input.read_real();
    let theta: Real = input.read_real() / 180 * Real::PI;
    let x = input.read_real();
    let h1 = input.read_real();
    let h2 = input.read_real();

    let t = x / (v0 * theta.cos());
    let y = v0 * t * theta.sin() - t * t * 0.5 * 9.81;
    if h1 <= y - 1 && y + 1 <= h2 {
        out.print_line("Safe");
    } else {
        out.print_line("Not Safe");
    }
}

pub static TEST_TYPE: TestType = TestType::MultiNumber;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    let mut pre_calc = ();

    match TEST_TYPE {
        TestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        TestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
    }
    output.flush();
    match TASK_TYPE {
        TaskType::Classic => input.is_empty(),
        TaskType::Interactive => true,
    }
}

