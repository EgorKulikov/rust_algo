use algo_lib::collections::segment_tree::{Pushable, SegmentTree, SegmentTreeNode};
use algo_lib::io::input::Input;
use algo_lib::io::output::output;
use algo_lib::numbers::matrix::Matrix;
use algo_lib::numbers::mod_int::ModIntF;
use algo_lib::numbers::num_traits::zero_one::ZeroOne;
use algo_lib::out_line;

fn solve(input: &mut Input, _test_case: usize) {
    type Mod = ModIntF;

    let n = input.read_size();
    let q = input.read_size();
    let f = input.read_vec::<(Mod, Mod)>(n);

    #[derive(Clone)]
    struct Node(Matrix<Mod>);

    impl SegmentTreeNode for Node {
        fn new(_left: usize, _right: usize) -> Self {
            Self(Matrix::ident(2))
        }

        fn join(
            &mut self,
            left_val: &Self,
            right_val: &Self,
            _left: usize,
            _mid: usize,
            _right: usize,
        ) {
            Matrix::do_mult(&mut self.0, &left_val.0, &right_val.0);
        }

        fn accumulate(&mut self, _value: &Self, _left: usize, _right: usize) {}

        fn reset_delta(&mut self, _left: usize, _right: usize) {}
    }

    impl Pushable<Matrix<Mod>> for Node {
        fn push(&mut self, delta: Matrix<Mod>, _left: usize, _right: usize) {
            *self = Node(delta);
        }
    }

    let mut tree = SegmentTree::from_generator(n, |i| {
        let (a, b) = f[i];
        Node(Matrix::new(&[&[a, Mod::zero()], &[b, Mod::one()]]))
    });
    for _ in 0..q {
        let t = input.read_size();
        if t == 0 {
            let p = input.read_size();
            let (c, d) = input.read::<(Mod, Mod)>();
            tree.point_update(p, Matrix::new(&[&[c, Mod::zero()], &[d, Mod::one()]]));
        } else {
            let l = input.read_size();
            let r = input.read_size();
            let x = input.read();
            let m = tree.query(l..r).0;
            let v = Matrix::new(&[&[x, Mod::one()]]);
            let res = v.mult(&m);
            out_line!(res[(0, 0)]);
        }
    }
}

pub(crate) fn run(mut input: Input) -> bool {
    #[allow(dead_code)]
    enum TestType {
        Single,
        MultiNumber,
        MultiEof,
    }
    let test_type = TestType::Single;
    match test_type {
        TestType::Single => solve(&mut input, 1),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 0usize..t {
                solve(&mut input, i + 1);
            }
        }
        TestType::MultiEof => {
            let mut i = 1usize;
            while input.peek().is_some() {
                solve(&mut input, i);
                i += 1;
            }
        }
    }
    output().flush();
    input.skip_whitespace();
    !input.peek().is_some()
}

