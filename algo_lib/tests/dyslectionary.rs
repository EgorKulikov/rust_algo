type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, test_case: usize, _data: &mut PreCalc) {
    let s = input
        .iter::<EolStr>()
        .take_while(|x| !x.is_empty())
        .map(|x| {
            let mut s = x.unwrap();
            s.reverse();
            s
        })
        .collect::<Vec<_>>()
        .sorted();

    let max_len = s.iter().map(|x| x.len()).max().unwrap();
    if test_case != 1 {
        out.print_line(());
    }
    for mut s in s {
        let len = s.len();
        s.extend(std::iter::repeat(b' ').take(max_len - len));
        s.reverse();
        out.print_line(s);
    }
}

pub static TEST_TYPE: TestType = TestType::MultiEof;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    let mut pre_calc = ();

    match TEST_TYPE {
        TestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        TestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
    }
    output.flush();
    match TASK_TYPE {
        TaskType::Classic => input.is_empty(),
        TaskType::Interactive => true,
    }
}

