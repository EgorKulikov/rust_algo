use algo_lib::io::input::Input;
use algo_lib::io::output::output;
use algo_lib::out_line;

fn solve(input: &mut Input, _test_case: usize) {
    let mut pts = 0;

    while !input.is_exhausted() {
        let opp = input.read_char();
        let we = input.read_char();

        let opp = (opp as i32) - ('A' as i32);
        let we = (we as i32) - ('X' as i32);
        pts += we + 1;
        if opp == we {
            pts += 3;
        } else if (opp + 1) % 3 == we {
            pts += 6;
        }
        input.skip_whitespace();
    }
    out_line!(pts);
}

pub(crate) fn run(mut input: Input) -> bool {
    #[allow(dead_code)]
    enum TestType {
        Single,
        MultiNumber,
        MultiEof,
    }
    let test_type = TestType::Single;
    match test_type {
        TestType::Single => solve(&mut input, 1),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 0usize..t {
                solve(&mut input, i + 1);
            }
        }
        TestType::MultiEof => {
            let mut i = 1usize;
            while input.peek().is_some() {
                solve(&mut input, i);
                i += 1;
            }
        }
    }
    output().flush();
    input.skip_whitespace();
    !input.peek().is_some()
}

