use algo_lib::collections::bit_set::BitSet;
use algo_lib::collections::indexed_heap::IndexedHeap;
use algo_lib::collections::vec_ext::detuple::Detuple;
use algo_lib::io::input::Input;
use algo_lib::io::output::Output;
use algo_lib::misc::test_type::TaskType;
use algo_lib::misc::test_type::TestType;
use std::cmp::Reverse;

type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let n = input.read_size();
    let ab = input.read_long_pair_vec(n);

    let (a, b) = ab.detuple();
    let mut heap = IndexedHeap::new(n);
    for i in 0..n {
        heap.add_or_adjust(i, Reverse(a[i]));
    }
    let mut ans = 0;
    let mut done = BitSet::new(n);
    while let Some((pos, Reverse(val))) = heap.pop() {
        ans += val;
        done.set(pos);
        if pos > 0 && !done[pos - 1] {
            heap.add_or_relax(pos - 1, Reverse(val - b[pos - 1]));
        }
        if pos + 1 < n && !done[pos + 1] {
            heap.add_or_relax(pos + 1, Reverse(val - b[pos + 1]));
        }
    }
    out.print_line(ans);
}

pub static TEST_TYPE: TestType = TestType::MultiNumber;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    eprint!("\x1B[33m\x1B[03m");

    let mut pre_calc = ();
    // output.set_bool_output(BoolOutput::YesNo);

    match TEST_TYPE {
        TestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        TestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
        _ => {
            unreachable!();
        }
    }
    eprint!("\x1B[0m");
    output.flush();
    input.check_empty()
}

