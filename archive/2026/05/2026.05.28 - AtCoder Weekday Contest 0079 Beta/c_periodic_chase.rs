type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let n = input.read_size();
    let q = input.read_size();
    let (a, b) = input.read_long_pair_vec(n).detuple();

    let mut delta = Vec::with_gen(n, |i| b[i] - a[i]);
    let mut sum = delta.copy_sum();
    for _ in 0..q {
        let t = input.read_int();
        match t {
            1 => {
                let i = input.read_size() - 1;
                let a = input.read_long();
                let b = input.read_long();
                sum -= delta[i];
                delta[i] = b - a;
                sum += delta[i];
            }
            2 => {
                let d = input.read_long();
                let mut ans = None;
                let mut cur = 0;
                for i in 0..n {
                    cur += delta[i];
                    if cur >= d {
                        ans.minim(i + 1);
                    } else if sum > 0 {
                        let add = (d - cur).upper_div(sum);
                        ans.minim(i + 1 + add as usize * n);
                    }
                }
                out.print_line(ans);
            }
            _ => unreachable!(),
        }
    }
}

pub static TEST_TYPE: TestType = TestType::Single;
pub static TASK_TYPE: TaskType = TaskType::Classic;

pub(crate) fn run(mut input: Input, mut output: Output) -> bool {
    eprint!("\x1B[33m\x1B[03m");
    let mut pre_calc = ();
    output.set_bool_output(BoolOutput::YesNo);

    match TEST_TYPE {
        TestType::Single => solve(&mut input, &mut output, 1, &mut pre_calc),
        TestType::MultiNumber => {
            let t = input.read();
            for i in 1..=t {
                solve(&mut input, &mut output, i, &mut pre_calc);
            }
        }
        TestType::MultiEof => {
            let mut i = 1;
            while input.peek().is_some() {
                solve(&mut input, &mut output, i, &mut pre_calc);
                i += 1;
            }
        }
        _ => {
            unreachable!();
        }
    }
    eprint!("\x1B[0m");
    output.flush();
    input.check_empty()
}

#[cfg(feature = "local")]
mod tester;

#[cfg(feature = "local")]
fn main() {
    tester::run_tests();
}

#[cfg(not(feature = "local"))]
fn main() {
    #[cfg(debug_assertions)]
    eprintln!("Library code is available at https://github.com/EgorKulikov/rust_algo");
    let input = algo_lib::io::input::Input::stdin();
    let output = algo_lib::io::output::Output::stdout();
    run(input, output);
}
