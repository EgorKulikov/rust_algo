type PreCalc = ();

fn solve(input: &mut Input, out: &mut Output, _test_case: usize, _data: &mut PreCalc) {
    let mut s = input.read_long();
    let mut m = input.read_long();

    let mut ans = 0;
    for i in (0..60).rev() {
        if m.is_set(i) {
            ans.maxim(s.upper_div(m));
            m.unset_bit(i);
            for j in i..60 {
                s.unset_bit(j);
            }
        }
    }
    if s != 0 {
        out.print_line(-1);
    } else {
        out.print_line(ans);
    }
}

